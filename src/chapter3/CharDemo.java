package chapter3;

// Продемонстрировать применение типа данных char
public class CharDemo {
    public static void main(String[] args) {
        char chl, ch2;
        chl = 88 + 1; // код символа Х
        ch2 = 'у';
        System.out.print("chl и ch2: ");
        System.out.println(chl + " " + ch2);
        char s, i, r;
        s = 83;
        i = 73;
        r = 82;
        System.out.println("im anunne " + s + i + r);
    }
}
